# Сортировка: длинные фразы первыми
REPLACEMENTS.sort(key=lambda x: -len(x[0]))


# Замены только по границам слов (чтобы не заменить "демон" внутри "продемонстрировал")
WORD_BOUNDARY_REPLACEMENTS = [
    (r"\bДемон\b", "Алкаш"),
    (r"\bдемон\b", "алкаш"),
    (r"\bдемона\b", "алкаша"),
    (r"\bдемону\b", "алкашу"),
    (r"\bдемоном\b", "алкашом"),
    (r"\bДемоны\b", "Алкаши"),
    (r"\bдемоны\b", "алкаши"),
    (r"\bдемонов\b", "алкашей"),
    (r"\bдемонам\b", "алкашам"),
    (r"\bдемонами\b", "алкашами"),
    (r"\bдемонах\b", "алкашах"),
]


def apply_replacements(text: str) -> str:
    """Применяет все замены к тексту."""
    for old, new in REPLACEMENTS:
        if old in text:
            text = text.replace(old, new)
    # Замены по границам слов для коротких форм (избегаем "продемонстрировал" -> "проалкашстрировал")
    for pattern, repl in WORD_BOUNDARY_REPLACEMENTS:
        text = re.sub(pattern, repl, text, flags=re.IGNORECASE)
    return text


def process_file(path: Path) -> Path | None:
    """Обрабатывает один файл. Возвращает путь к новому файлу или None."""
    if not path.suffix == ".txt" or path.name.startswith("edited_"):
        return None
    content = path.read_text(encoding="utf-8")
    new_content = apply_replacements(content)
    new_name = f"edited_{path.name}"
    out_path = path.parent / new_name
    out_path.write_text(new_content, encoding="utf-8")
    return out_path


def main():
    processed = []
    for f in sorted(BASE.iterdir()):
        if f.is_file() and f.suffix == ".txt" and not f.name.startswith("edited_"):
            result = process_file(f)
            if result:
                processed.append((f.name, result.name))
                print(f"  {f.name} -> {result.name}")
    print(f"\nОбработано файлов: {len(processed)}")
    return processed


if __name__ == "__main__":
    main()
